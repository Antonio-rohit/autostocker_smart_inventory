  monthlySales.map((sales, index) => ({
    id: `${skuPrefix}-${index + 1}`,
    month: ["Sep", "Oct", "Nov", "Dec", "Jan"][index],
    sales,
    revenue: Number((sales * price).toFixed(2)),
  }));

